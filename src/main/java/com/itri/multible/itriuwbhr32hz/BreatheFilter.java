	public void InitBrFilter()
	{
		int i;

		for(i = 0; i < n; i++)
			dbuffer[i] = 0;
	}

	//===============================================================================================//
	//==  Function : BrFilter
	//==  Description : Direct form II Tchebyshev filter 0.1~0.8 Hz (fs = 64Hz)
	//==   Parameters:
	//==      indata 
	//===============================================================================================//
	private int sumdata = 0, indatacount = 0;
	private double avgdata = 0;
	public double chebyFilterBr(double indata)
	{

		int i, j, k;
		double b_dbuffer;

		double inputDouble = indata;

		sumdata= (int) (sumdata+inputDouble);
		indatacount++;
		if (indatacount==31)
		{
			avgdata= sumdata / 32f;
			indatacount=0;
			sumdata=0;
		}

		inputDouble=inputDouble-avgdata;


		for (k = 0; k < n - 1; k++) {
			dbuffer[k] = dbuffer[k + 1];//n=7
		}
		dbuffer[n - 1] = 0.0;
		for (k = 0; k < n; k++) {
			b_dbuffer = dbuffer[k] + inputDouble * array_dv0[type][k];
			dbuffer[k] = b_dbuffer;
		}

		for (k = 0; k < n - 1; k++) {
			dbuffer[k + 1] -= dbuffer[0] * array_dv1[type][k + 1];
		}

//		Log.i("BreathFilter", "type = " + type);
		return(dbuffer[0]);
	}

